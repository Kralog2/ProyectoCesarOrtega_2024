        <h3>Who are Bethesda?</h3>
                    <img src="recursos/media/bethesdalogo2.png" alt="Logo de Bethesda">
                    <p>For two decades, Bethesda Softworks has been a developer and distributor of interactive entertainment content, primarily video games for consoles and computers. It was founded in 1986 by Christopher Weaver in Bethesda, Maryland, and its headquarters were later moved to Rockville, Maryland, in 1990. In 1999, Weaver, together with his partner Robert A. Altman, founded ZeniMax Media, leaving Bethesda as a subsidiary of that company.</p>
                    <p>It is known for its sports simulation, action and role-playing games. Some of its best known works are the videogames of The Elder Scrolls series, as well as others such as DOOM, Fallout and Wolfenstein.</p>
                    <p>Bethesda has been recognized since its creation for the development of the first sports video game based on a physics engine, Gridiron!, in 1986 for Atari ST, Commodore Amiga and Commodore 64/128. Its first works were well received by the specialized press; for example, in various reviews of the Wayne Gretzky Hockey video game, it was described as “an excellent game” or, in the case of the Amiga Computing review, “the most accurate and fun sports simulation I have had the pleasure of playing”. The company is best known for creating The Elder Scrolls series of role-playing video games, based on the original work of Julian Lefay. The first chapter of the series, called The Elder Scrolls: Arena, was released in 1994. Since then, thanks to its success, numerous related video games have been published. The direct sequels, Daggerfall, Morrowind, Oblivion, and Skyrim were released in 1996, 2002, 2006 and 2011, respectively. In addition, the series has had several related spin-offs: An Elder Scrolls Legend: Battlespire in 1997, The Elder Scrolls Adventures: Redguard in 1998, and The Elder Scrolls Travels: Shadowkey in 2004. The studio is also known for publishing titles based on popular movie franchises, such as The Terminator, Star Trek, Pirates of the Caribbean or Mad Max.</p>
                    <p>In 2004, Bethesda Softworks acquired the Fallout series of role-playing video games, previously owned by Interplay Productions, handing over the development of Fallout 3 to in-house studio Bethesda Game Studios. The game was finally released on October 28, 2008. The following year, five content expansions were released for the game: Operation: Anchorage, The Pitt, Broken Steel, Point Lookout, and Mothership Zeta. On June 24, 2009, ZeniMax Media, Bethesda's parent company, acquired the id Software development studio, whose titles, including Rage, would be published by Bethesda Softworks from that moment on.</p>
