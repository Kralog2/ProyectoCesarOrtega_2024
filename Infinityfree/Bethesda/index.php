                    <h3>Wolfenstein</h3>
                    <img src="recursos/media/Wolfenstein.jpg" alt="Portada de Wolfenstein">
                    <p>Wolfenstein es una serie de videojuegos de la Segunda Guerra Mundial desarrollada originalmente por Muse Software. La mayoría de los juegos siguen a William «B.J.» Blazkowicz, un capitán del ejército estadounidense, y su lucha contra las potencias del Eje. Los primeros títulos se centran en el intento de los nazis de aprovechar las fuerzas sobrenaturales y ocultas, mientras que los últimos juegos se ambientan en una historia alternativa en la que las potencias del Eje ganaron la Segunda Guerra Mundial.</p>
                    <p>os dos primeros juegos de la serie, Castle Wolfenstein y Beyond Castle Wolfenstein, se centraban en el sigilo desde una perspectiva vertical. A partir de Wolfenstein 3D, de id Software, se pasó al género de los shooters en primera persona, que ayudó a popularizar. Después de que ZeniMax Media adquiriera id Software, incluida la franquicia Wolfenstein, la desarrolladora MachineGames se convirtió en la principal desarrolladora de la serie.</p>
