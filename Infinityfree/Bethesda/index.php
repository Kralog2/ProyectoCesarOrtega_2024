                    <h3>Doom</h3>
                    <img src="recursos/media/Doom.jpg" alt="Portada de Doom">
                    <p>La franquicia Doom (estilizada DooM o DOOM) es una serie de videojuegos de disparos en primera persona desarrollados por id Software. Esta franquicia incluye novelas, cómics, juegos de mesa y adaptaciones cinematográficas. La serie se centra en las hazañas de un marine espacial no identificado que opera bajo las órdenes de la Union Aerospace Corporation (UAC), que lucha contra hordas de demonios y no muertos en el inframundo y la Tierra, protagonizando diversos viajes por portales entre ambos mundos.</p>
