                    <h3>Fallout</h3>
                    <img src="recursos/media/fallout-logo-10AECC0B13-seeklogo.com.png" alt="Logo de fallout">
                    <p>La serie Fallout comenzó en 1997 con los juegos de rol en perspectiva isométrica para PC desarrollados por Black Isle Studios e Interplay Entertainment. Tras un largo periodo de ausencia, Bethesda Game Studios, creadores de The Elder Scrolls, se hicieron con los derechos de esta anacronía posnuclear ambientada en Estados Unidos. En 2008 publicaron Fallout 3, que renovó la serie como un RPG de acción, una fórmula con la que se han publicado varias entregas. También hay juegos derivados o spin-offs de otros géneros, como la gestión de Fallout Shelter y la acción de Fallout: Brotherhood of Steel, e incluso series de televisión.</p>
